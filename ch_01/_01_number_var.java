package ch_01;

public class _01_number_var {
    public static void main(String[] args) {

        // 정수 표현
        // "int, long", byte, short

        int age = 10;
        long money = 100000000000000L;
        // long 을 쓸때는 뒤에 L을 부어주어야 한다.

        // 실수 표현
        // float, double

        float f = 3.14F; // float을 쓸때 뒤에 F를 붙여주어야 한다
        double d = 3.14555e2; // e(숫자)는 10의 거듭제곱을 의미

        int octal = 023; // 앞에 0 을 붙이면 8진수
        int hex = 0xC; // 앞에 0x 를 붙이면 16진수

        // 사칙연산 및 증감자 사용가능
    }
}